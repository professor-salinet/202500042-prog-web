// Operadores de incremento/decremento

let contador = 0;

contador++; // contador = contador + 1; // 1
console.log(contador);

contador += 5; // contador = contador + 5; // 6
console.log(contador);

contador -= 3; // contador = contador - 3; // 3
console.log(contador);

contador--; // contador = contador - 1; // 2
console.log(contador);

